			<h2>General Motors pagará multa de US$ 1 bilhão por defeito</h2>
			<p>Washington, 16 set (EFE).- A General Motors (GM) pagará uma multa de aproximadamente US$ 1 bilhão às autoridades federais americanas pelo defeito no sistema de ignição que afetou 2,6 milhões de veículos da fabricante e que causou pelo menos 124 mortes.</p>

			<p>De acordo com o jornal "The Detroit News", as autoridades devem anunciar na quinta-feira (17) um acordo com a GM no qual a fabricante será acusada por dois delitos e pagará US$ 1 bilhão de multa.</p>

			<p>O anúncio ocorre poucas semanas depois de o advogado Kenneth Feinberg, encarregado pela GM para estabelecer de forma independente que pessoas receberão indenização pelo defeito, concluir seus trabalhos.</p>

			<p>Em 24 de agosto, Feinberg anunciou que após meses de trabalhos o fundo de indenização da GM reconhecerá 399 processos, entre eles 124 por morte. Os outros são 17 por lesões muito graves e 258 por lesões graves.</p>

			<p>Ao mesmo tempo, Feinberg recusou 349 solicitações de indenização por falecimento, 262 por lesões muito graves e 3.333 por lesões graves.</p>

			<p>Em 2014, a GM reconheceu publicamente a existência do defeito, e após uma investigação interna, determinou que havia empregados da companhia que conheciam o problema há dez anos. Sendo assim, a empresa estabeleceu o fundo para compensar as vítimas.</p>

			<p>O defeito do sistema de ignição afetou modelos produzidos pelas marcas da GM antes da criação da nova General Motors em 2009, e permite a desativação involuntária e repentina do motor do veículo, o que desliga seus airbags. O defeito teria causado centenas de acidentes nos Estados Unidos.</p>

			<p>Em maio deste ano, a GM já havia firmado com a Administração Nacional para a Segurança na Estrada dos Estados Unidos (NHTSA) o pagamento de uma multa de US$ 35 milhões por ocultar durante anos a informação sobre o defeito. </p>
