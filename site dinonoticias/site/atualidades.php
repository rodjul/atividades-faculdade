			<p>Já pensou em alugar o quarto vazio da sua casa para um turista? Almoçar na casa de um estranho que cozinhe para você ou compartilhar um escritório por algumas horas com outras pessoas? Ou compartilhar um serviço de transporte?</p>

			<p>Serviços como o polêmico Uber, que pode conectar diversos passageiros a um motorista particular, CouchSurfing, que hospeda pessoas em residências, ou o Airbnb, aplicativo que permite às pessoas alugarem sua própria casa para turistas, fazem parte da economia compartilhada, também conhecida como economia colaborativa.</p>

			<p>Trata-se de um novo jeito de consumir e de um novo modelo econômico com negócios ligados à prática do compartilhamento. O consumidor não adquire a posse dos bens, mas o seu acesso. Isso porque a ideia é dividir, alugar ou partilhar o uso de produtos e serviços com os outros.</p>

			<p>A partir desse modelo, pessoas físicas podem ter relações diretas de compra e venda de qualquer tipo de demanda. Uma das consequências é a descentralização do fluxo entre clientes e empresas, evitando o papel de intermediários e aproximando consumidores e fornecedores.</p>

			<p>O motor principal dessa economia é a tecnologia digital. Surgida na década de 1990, as primeiras plataformas online de venda de produtos pela internet começaram a abrir o caminho para a economia compartilhada, como o site eBay, que vende produtos usados, e a Craiglists, página de classificados.</p>
