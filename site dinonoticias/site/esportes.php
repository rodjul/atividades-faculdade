			<h2>Morte do "Gaúcho da Copa" repercute em homenagem até da seleção alemã</h2>
			<h4>16/09/2015 17h44 - Atualizado em 16/09/2015 18h47</h4>
			<figure>
				<img src="http://s2.glbimg.com/x9EALF7OCOm5C1JqZaxxgtY0kyk=/0x0:1168x714/320x196/s.glbimg.com/es/ge/f/original/2015/09/16/gaucho3.jpg" />
