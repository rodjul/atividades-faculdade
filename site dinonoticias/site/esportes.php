			<p>A morte de Clóvis Acosta Fernandes, mais conhecido como o "Gaúcho da Copa", repercutiu na imprensa mundial e em órgãos ligados ao futebol durante esta quarta-feira. Aos 60 anos, o homem que acompanhou a seleção brasileira em sete Copas e tornou-se conhecido pela simpatia e por carregar um troféu consigo nos estádios, além da indumentária típica do Rio Grande do Sul, lutava contra um câncer há nove anos.</p>
			<p>A Fifa prestou homenagens em seu perfil oficial no Twitter e também pelo Instagram. A entidade máxima do futebol também relembrou um vídeo gravado com Clóvis em preparação à Copa do Mundo de 2014. O órgão considerava o torcedor o "12º jogador" da equipe verde-amarela.</p>
			<p>Responsável pelo vexame brasileiro na semifinal do Mundial do ano passado, ao aplicar 7 a 1 no time de Luiz Felipe Scolari no Mineirão, a seleção alemã também lembrou do "Gaúcho da Copa" em seu Twitter. No texto, os europeus lembram que a emoção de Clóvis comoveu a todos.</p>
			<p>Apesar de ser um símbolo nacional, o torcedor jamais escondeu sua paixão pelo Grêmio. O tricolor gaúcho lhe rendeu préstimos nas redes sociais. "O teu amor pelo Grêmio e pelo Rio Grande do Sul nunca será esquecido", aparece na imagem postada pelo clube.</p>
			<p>A imprensa internacional noticiou a morte de Clóvis e destacou-o também como um dos torcedores mais ilustres. Lembrou o bigode característico do brasileiro e a disposição que tinha para rodar o mundo atrás da seleção brasileira. Além dos principais jornais da imprensa britânica, como Mirror e The Independent, a lembrança de Clóvis chegou em sites como Bleacher Report e Eurosport.</p>
